#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		a;
	char	*l;

	a = 0;
	l = (char *)s;
	while (l[a] != (char)c)
	{
		if (l[a] == 0)
		{
			return (0);
		}
		a++;
	}
	return (&l[a]);
}
